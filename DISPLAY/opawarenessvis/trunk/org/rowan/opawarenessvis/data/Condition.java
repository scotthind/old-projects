package org.rowan.opawarenessvis.data;

/**
 * Condition is an Enum defining all of the possible types of a threshold condition.
 * 
 * @author Dan Urbano
 */
public enum Condition{
    GreaterThan,
    GreaterThanOrEqualTo,
    LessThan,
    LessThanOrEqualTo,
    EqualTo,
    AtLeast,
    AtMost
}